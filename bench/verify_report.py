#!/usr/bin/env python3
"""
CU Benchmark Suite — Report Integrity Verifier

Verifies that a benchmark_report.json has not been modified since it was
generated by the CU benchmark suite. Usable by any third party with only
Python 3 — no dependencies required.

Usage:
    python3 verify_report.py benchmark_report.json
    python3 verify_report.py /path/to/results/run_20260301T120000Z_abc12345/benchmark_report.json

Exit codes:
    0  — report is intact (hash verified)
    1  — hash mismatch (report was modified after generation)
    2  — missing fields or bad format
"""
import hashlib
import json
import sys


INTEGRITY_FIELDS = {"integrity_sha256", "integrity_method"}


def verify(path: str) -> bool:
    try:
        with open(path, encoding="utf-8") as f:
            data = json.load(f)
    except FileNotFoundError:
        print(f"ERROR: File not found: {path}")
        sys.exit(2)
    except json.JSONDecodeError as e:
        print(f"ERROR: Invalid JSON: {e}")
        sys.exit(2)

    stored_hash = data.get("integrity_sha256")
    if not stored_hash:
        print("ERROR: No integrity_sha256 field found. Report was not generated by cu-bench v1.0+.")
        sys.exit(2)

    # Reconstruct the exact content that was hashed at generation time:
    # Remove all integrity fields — hash covers everything else.
    payload = {k: v for k, v in data.items() if k not in INTEGRITY_FIELDS}

    # Canonical serialization — identical to what report.py produced.
    canonical = json.dumps(payload, indent=2, sort_keys=True, ensure_ascii=True)
    computed_hash = hashlib.sha256(canonical.encode("utf-8")).hexdigest()

    # ─── Report identity ───
    run_id    = data.get("run_id", "unknown")
    timestamp = data.get("timestamp", "unknown")
    gpu       = data.get("environment", {}).get("gpu_model", "unknown")
    gpu_count = data.get("environment", {}).get("gpu_count", "?")

    print(f"\n  CU Benchmark Report Verifier")
    print(f"  {'─' * 44}")
    print(f"  File:      {path}")
    print(f"  Run ID:    {run_id}")
    print(f"  Timestamp: {timestamp}")
    print(f"  Hardware:  {gpu} × {gpu_count}")
    print(f"  {'─' * 44}")
    print(f"  Stored hash:   {stored_hash}")
    print(f"  Computed hash: {computed_hash}")
    print()

    if computed_hash == stored_hash:
        print("  ✓  VERIFIED — report is intact. Numbers match the original benchmark run.")
        print()
        _print_key_measurements(data)
        return True
    else:
        print("  ✗  FAILED — hash mismatch. Report was modified after generation.")
        print()
        print("  This means one or more measured values were changed after the benchmark")
        print("  completed. The stored numbers cannot be trusted as original measurements.")
        return False


def _print_key_measurements(data: dict) -> None:
    """Print the key measurements that are covered by the integrity seal."""
    specs = data.get("measured_specs", {})
    if not specs:
        return

    def v(key, unit=""):
        val = specs.get(key)
        if val is None:
            return "-"
        return f"{val} {unit}".strip()

    print("  Sealed measurements:")
    print(f"    FP32:         {v('fp32_tflops', 'TFLOPS')}")
    print(f"    FP16:         {v('fp16_tflops', 'TFLOPS')}")
    print(f"    BF16:         {v('bf16_tflops', 'TFLOPS')}")
    print(f"    FP8:          {v('fp8_tflops', 'TFLOPS')}")
    print(f"    Mem BW:       {v('membw_gbps', 'GB/s')}")
    print(f"    VRAM:         {v('vram_usable_gb', 'GB')}")
    print(f"    Interconnect: {v('interconnect_bw_gbps', 'GB/s')}  [{specs.get('interconnect_source', '')}]")
    print(f"    Inference:    {v('mtok_per_hour', 'Mtok/hr')}")
    print(f"    Eff. Factor:  {v('efficiency_factor')}")
    print()
    completed = data.get("benchmarks_completed", [])
    print(f"  Benchmarks completed: {', '.join(completed) if completed else 'none listed'}")
    print()


if __name__ == "__main__":
    if len(sys.argv) < 2:
        print(__doc__)
        sys.exit(2)

    ok = verify(sys.argv[1])
    sys.exit(0 if ok else 1)
